const deleteOldImageFromCloudinary = async (cloudinaryFilePath) => {
  try {
    const public_id = getFileIdFromUrl(cloudinaryFilePath);
    if (!public_id) {
      console.log(
        "File public_id not found, please provide a valid cloudinary fileurl."
      );
      return null;
    }
    const response = await cloudinary.uploader.destroy(public_id);
    console.log("response of file delete :", response);
    if (response.result == "ok") {
      console.log("Image Successfully deleted from cloudinary");
    } else {
      console.log(
        "The given file does not exist on cloudinary, it may already deleted."
      );
    }
    return response;
  } catch (error) {
    console.log("Error deleting file from Cloudinary : ", error);
  }
};

//code generated by chatgpt to extract fileId from url.
function getFileIdFromUrl(url) {
  // Define a regular expression to match the file ID in the URL
  const regex = /\/upload\/v\d+\/([^/]+)\./;

  // Execute the regex on the URL
  const match = url.match(regex);

  // Check if a match was found and return the file ID
  if (match && match[1]) {
    return match[1];
  } else {
    // Return null or an appropriate value if no match was found
    return null;
  }
}

